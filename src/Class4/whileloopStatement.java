package Class4;

public class whileloopStatement {
    public static void main(String[] args) {
//Java for loop: for(initailalization, Condition, increment/decerement)

        int i = 0;
        while (i<9){
            System.out.println(i);
            i++;
        }
        System.out.println("==============================================");
        int j =0;
        do {
            System.out.println(i );
            j++;
        } while (j<=9);
    }
}
