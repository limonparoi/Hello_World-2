public class ArithmeticOperatorTest {
    public static void main(String[] args) {
        int number1=1000, number2=20, number;
        number=number1-number2;
        System.out.println(number);

        number = number-number1;
        System.out.println(number);

        number = number/number2;
        System.out.println(number);

        number = number1*number2;
        System.out.println(number);

        number = number%number2;
        System.out.println(number);
    }
}
