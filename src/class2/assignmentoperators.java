package class2;

public class assignmentoperators {
    public static void main(String[] args) {
        int x = 10;

        x +=5;
        System.out.println(x);//output 15

        x+=3;
        System.out.println(x);//output 18

        x-=3;
        System.out.println(x);//output 15

        x*=2;
        System.out.println(x);//output 30

        x/=3;
        System.out.println(x);//output 5
        x%=3;
        System.out.println(x);//output 5
        x&=3;
        System.out.println(x);//output 5

    }
}
